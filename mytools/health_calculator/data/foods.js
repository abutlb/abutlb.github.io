export function getFoodRecommendations(data, metrics) {
  const { bmi } = metrics;
  const goal = data.primaryGoal;
  const foods = [];

  if (bmi < 18.5 || goal === 'weight_gain' || goal === 'muscle_gain') {
    foods.push(
      { name: 'المكسرات والبذور',         icon: '🥜', benefit: 'غنية بالسعرات والدهون الصحية والبروتين' },
      { name: 'الأفوكادو',                icon: '🥑', benefit: 'دهون أحادية غير مشبعة وسعرات عالية' },
      { name: 'الشوفان بالحليب والموز',   icon: '🥣', benefit: 'كربوهيدرات معقدة + بروتين + بوتاسيوم' },
      { name: 'صدر الدجاج أو السلمون',   icon: '🍗', benefit: 'بروتين عالي الجودة لبناء العضلات' },
      { name: 'البيض الكامل',             icon: '🥚', benefit: 'بروتين كامل + دهون صحية + فيتامين د' },
      { name: 'الجبن القريش أو اللبن',   icon: '🧀', benefit: 'كالسيوم + بروتين سريع الامتصاص' },
    );
  } else if (bmi >= 25 || goal === 'weight_loss') {
    foods.push(
      { name: 'الخضروات الورقية الداكنة', icon: '🥬', benefit: 'قليلة السعرات وغنية بالألياف والحديد' },
      { name: 'الأسماك الدهنية (سلمون، ماكريل)', icon: '🐟', benefit: 'أوميغا3 + بروتين عالٍ يعزز الشبع' },
      { name: 'التوت (بلوبيري، فراولة)',  icon: '🍓', benefit: 'مضادات أكسدة قوية وقليل السعرات' },
      { name: 'البروكلي والقرنبيط',       icon: '🥦', benefit: 'ألياف + فيتامين ج + مركبات محاربة للسرطان' },
      { name: 'البقوليات (عدس، حمص)',     icon: '🫘', benefit: 'ألياف + بروتين نباتي + تحكم في السكر' },
      { name: 'اللبن اليوناني خالي الدسم', icon: '🥛', benefit: 'بروتين عالٍ + بكتيريا نافعة للأمعاء' },
    );
  } else {
    foods.push(
      { name: 'الحبوب الكاملة (كينوا، شوفان)', icon: '🌾', benefit: 'كربوهيدرات معقدة + ألياف + طاقة مستدامة' },
      { name: 'الخضروات الملونة المتنوعة', icon: '🥕', benefit: 'فيتامينات ومعادن لكل وظائف الجسم' },
      { name: 'الأسماك مرتين أسبوعياً',  icon: '🐟', benefit: 'أوميغا3 + فيتامين د + يود' },
      { name: 'البيض والبقوليات',          icon: '🥚', benefit: 'بروتين متنوع المصادر لصحة العضلات' },
      { name: 'المكسرات النيئة (لوز، جوز)', icon: '🌰', benefit: 'دهون صحية + فيتامين هـ + مغنيسيوم' },
      { name: 'الفواكه الطازجة الموسمية', icon: '🍊', benefit: 'فيتامين ج + ألياف + سكريات طبيعية' },
    );
  }

  if (data.activityLevel === 'active' || data.activityLevel === 'very_active') {
    foods.push(
      { name: 'الموز قبل التمرين',        icon: '🍌', benefit: 'كربوهيدرات سريعة + بوتاسيوم لمنع التشنج' },
      { name: 'حليب الشوكولاتة بعد التمرين', icon: '🍫', benefit: 'نسبة كارب/بروتين مثالية للتعافي' },
    );
  }

  return foods;
}
